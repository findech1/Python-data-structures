# Creating a tuple
fruits = ("apple", "banana", "cherry")

# Accessing tuple elements
print(fruits[1])

# Looping through a tuple
for fruit in fruits:
    print(fruit)

# Tuple slicing
print(fruits[1:])

# Tuple length
print(len(fruits))

# Checking if an item exists in a tuple
if "apple" in fruits:
    print("Yes, 'apple' is in the fruits tuple")
