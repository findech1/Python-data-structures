num1 = 3.142342
num2 = 10.23466
#previous
print('num 1 is', num1, 'and num 2 is', num2)

#format method using precision(:.), f is decimal point
print('num 1 is {0:.3f} and num 2 is {1:.3f}'.format(num1, num2))

#Using F-String
print(f'num 1 is {num1:.3f} and num 2 is {num2:.3f}')
print(f'num 1 is {num1:.4f} and num 2 is {num2:.4f}')