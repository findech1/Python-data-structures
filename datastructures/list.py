# Creating a list
my_list = [1, 2, 3, 4, 5]

# Accessing elements in the list
print(my_list[0])  # Output: 1

# Modifying an element in the list
my_list[2] = 10

# Appending an element to the list
my_list.append(6)

# Removing an element from the list
my_list.remove(4)

# Looping through the list
for element in my_list:
    print(element)
